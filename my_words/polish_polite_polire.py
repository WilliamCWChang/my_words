# polish （磨光）和 polite （有礼貌的）不但整体相似，而且的确同源
# 都来自拉丁语 polire "使光滑"。
# 磨光是对粗糙边角的修饰，
# 礼貌是对粗陋言行的修饰。


# polish,光澤,優雅,精良,擦亮,磨光,推敲,發亮,變光滑,波蘭的波蘭人
# polished,磨光的,擦亮的,完美的,精良的,優雅的,使光滑,擦亮,的過去式和過去分詞
# polishing,拋光
# polite,形容詞,有禮貌的,殷勤的,文雅的,上流的
# politeness,記,有教養的,文雅的,禮貌,優雅,類,掩飾不是誠實故意怠慢不是禮貌,有禮,優雅
# impolite,不禮貌的,無禮的
